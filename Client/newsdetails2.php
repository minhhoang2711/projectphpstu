					<h3>Đạp xe sao cho đúng cách</h3>
					<h4>Đăng ngày 20/12/2017 <span><a href="#">Hoàng Đức Minh</a></span></h4>
					<br>
					<h5>Đi xe đạp là một môn thể dục nâng cao sức khỏe. Nhưng bạn đã biết cách đi xe đạp như thế nào để có hiệu quả nhất?</h5>
					<p>Tư thế: Tư thế đi xe đạp sai không chỉ ảnh hưởng đến hiệu quả rèn luyện, mà còn rất dễ làm tổn thương cơ thể. Chẳng hạn như hai chân khuỳnh rộng, cúi đầu, vẹo lưng…đều là những tư thế không chuẩn xác. </p>
					<br>
					<p>Tư thế đúng đó là: Cơ thể hơi nghiêng về phía trước, hai cánh tay duổi thẳng, hóp chặt bụng, dùng cách thở bằng bụng, hai đùi song song với thanh ngang của xe, đầu gối, hông luôn phối hợp nhịp nhàng, đồng thời chú ý tới nhịp điệu đạp xem.</p>
					<br>
					<p>Động tác: Nhiều người cho rằng, đạp xe chính là chân đạp xuống dưới, bánh xe quay thì đạp. Thực ra, đạp xe chính xác bao gồm 4 động tác thống nhất: đạp, kéo, nâng, đẩy. Chân đạp xuống dưới, bàn chân co lại kéo lên, rồi nâng bàn đạp cuối cùng đẩy xuống, như vậy mới hoàn thành tròn một nhịp đạp xe. Như vậy đạp xe nhịp nhành không chỉ tiết kiệm sức lực mà còn đẩy nhanh tốc độ.</p>
					<br>
					<p>Tốc độ: Trên thực tế, nhiều người do bận rộn hoặc không để ý nên chỉ đạp xe dưới mức khả năng của mình. Điều này cũng tốt cho sức khỏe, nhưng sẽ là tốt hơn đến 3 lần nếu biết và đạp với hết khả năng của mình. Lấy ví dụ về một buổi đạp xe kéo dài trong 30 phút: 10 phút đầu đạp với tốc độ 20-25 km/h để làm nóng, và cũng là thời gian ra đến đường tập chính, 10 phút sau đó, đạp nhanh hết mức có thể. </p>
